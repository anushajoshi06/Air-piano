#include<avr/io.h>
#include<util/delay.h>
#include<util/timer.h>
#include<util/lcd.h>
#include<util/adc.h>
void main()
{
DDRC=0b11111111;
lcd_init();
adc_init();
timer_ctc_init();
while(1)
{
if((PINA&0b01111111)==0b00000001)
{
OCR0=260;
_delay_ms(1000);
}
if((PINA&0b01111111)==0b00000010)
{
OCR0=231;
_delay_ms(1000);
}
if((PINA&0b01111111)==0b00000100)
{
OCR0=208;
_delay_ms(1000);
}
if((PINA&0b01111111)==0b00001000)
{
OCR0=195;
_delay_ms(1000);
}
if((PINA&0b01111111)==0b00010000)
{
OCR0=173;
_delay_ms(1000);
}
if((PINA&0b01111111)==0b00100000)
{
OCR0=156;
_delay_ms(1000);
}
if((PINA&0b01111111)==0b01000000)
{
OCR0=138;
_delay_ms(1000);
}

}
}
